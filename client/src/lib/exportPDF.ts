import { jsPDF } from "jspdf";
import autoTable from "jspdf-autotable";

// Helper to format dates
const getFormattedDate = () => {
  const d = new Date();
  return d.toISOString().split("T")[0];
};

/**
 * Common PDF styling setup
 */
const setupPDFDoc = (title: string, subtitle: string, metaFields: { label: string; value: string }[]) => {
  const doc = new jsPDF("p", "pt", "a4");
  
  // Page boundaries (A4 size in points: 595 x 842)
  const margin = 20;
  
  // Logo Text
  doc.setFont("helvetica", "bold");
  doc.setFontSize(18);
  doc.setTextColor(124, 58, 237); // #7c3aed (purple)
  doc.text("SYNTRIX AI", margin, 35);

  // Subheader title
  doc.setFont("helvetica", "bold");
  doc.setFontSize(14);
  doc.setTextColor(17, 17, 17); // #111111
  doc.text(title, margin, 55);

  // Subtitle/Context
  doc.setFont("helvetica", "normal");
  doc.setFontSize(10);
  doc.setTextColor(100, 116, 139); // Slate-500
  doc.text(subtitle, margin, 70);

  // Meta info grid
  let currentY = 85;
  doc.setFont("helvetica", "bold");
  doc.setFontSize(9);
  doc.setTextColor(71, 85, 105); // Slate-600

  metaFields.forEach((meta) => {
    doc.text(`${meta.label}:`, margin, currentY);
    doc.setFont("helvetica", "normal");
    doc.setTextColor(17, 17, 17);
    doc.text(meta.value, margin + 80, currentY);
    currentY += 14;
    doc.setFont("helvetica", "bold");
    doc.setTextColor(71, 85, 105);
  });

  // Draw separator line
  doc.setDrawColor(226, 232, 240); // Slate-200
  doc.setLineWidth(1);
  doc.line(margin, currentY + 5, 575, currentY + 5);

  return { doc, startY: currentY + 25 };
};

/**
 * Add a standard section header to a PDF page
 */
const addSectionHeader = (doc: jsPDF, title: string, y: number): number => {
  doc.setFont("helvetica", "bold");
  doc.setFontSize(11);
  doc.setTextColor(124, 58, 237); // #7c3aed purple
  doc.text(title.toUpperCase(), 20, y);
  doc.setDrawColor(124, 58, 237);
  doc.setLineWidth(0.5);
  doc.line(20, y + 4, 120, y + 4);
  return y + 16;
};

/**
 * Draw footer on the current page
 */
const drawFooter = (doc: jsPDF, footerText: string = "Generated by Syntrix AI • syntrixai.com") => {
  const totalPages = (doc as any).internal.getNumberOfPages();
  for (let i = 1; i <= totalPages; i++) {
    doc.setPage(i);
    doc.setFont("helvetica", "normal");
    doc.setFontSize(8);
    doc.setTextColor(148, 163, 184); // Slate-400
    doc.text(footerText, 20, 825);
    doc.text(`Page ${i} of ${totalPages}`, 520, 825);
  }
};

/**
 * 1. ATS Report PDF Export
 */
export const generateATSReportPDF = (atsData: any, candidateName: string = "Executive Candidate") => {
  const { doc, startY } = setupPDFDoc(
    "ATS Analysis Report",
    "Candidate Resume Compatibility & Score Assessment",
    [
      { label: "Candidate", value: candidateName },
      { label: "Date Generated", value: new Date().toLocaleDateString() },
      { label: "Core Service", value: "Syntrix ATS Scanner Engine" }
    ]
  );

  let y = startY;

  // Section 1: Score Summary Table
  y = addSectionHeader(doc, "1. Score Summary", y);
  
  const scoreRows = [
    ["Overall ATS Score", `${atsData.scores?.overall || 0} / 100`],
    ["Technical Depth", `${atsData.scores?.technical_depth || 0} / 100`],
    ["ATS Optimization", `${atsData.scores?.ats_optimization || 0} / 100`],
    ["Recruiter Readability", `${atsData.scores?.recruiter_readability || 0} / 100`],
    ["Project Quality", `${atsData.scores?.project_quality || 0} / 100`]
  ];

  autoTable(doc, {
    startY: y,
    head: [["Performance Metric", "Score Grade"]],
    body: scoreRows,
    theme: "striped",
    headStyles: { fillColor: [124, 58, 237], textColor: [255, 255, 255], fontStyle: "bold" },
    bodyStyles: { textColor: [17, 17, 17], fontSize: 9 },
    alternateRowStyles: { fillColor: [249, 249, 249] },
    margin: { left: 20, right: 20 },
    didDrawPage: (data) => {
      y = data.cursor ? data.cursor.y + 25 : y + 100;
    }
  });

  // Section 2: Critical Keyword Gaps
  if (atsData.missing_keywords && atsData.missing_keywords.length > 0) {
    if (y > 750) { doc.addPage(); y = 40; }
    y = addSectionHeader(doc, "2. Critical Keyword Gaps", y);
    doc.setFont("helvetica", "normal");
    doc.setFontSize(9);
    doc.setTextColor(17, 17, 17);
    
    atsData.missing_keywords.forEach((keyword: string) => {
      doc.text(`• ${keyword}`, 30, y);
      y += 14;
    });
    y += 10;
  }

  // Section 3: Parsing & Formatting Issues
  if (atsData.formatting_issues && atsData.formatting_issues.length > 0) {
    if (y > 750) { doc.addPage(); y = 40; }
    y = addSectionHeader(doc, "3. Parsing & Formatting Issues", y);
    doc.setFont("helvetica", "normal");
    doc.setFontSize(9);
    doc.setTextColor(17, 17, 17);

    atsData.formatting_issues.forEach((issue: string) => {
      doc.text(`• ${issue}`, 30, y);
      y += 14;
    });
    y += 10;
  }

  // Section 4: Weak Bullets & Improvements
  if (atsData.weak_bullets && atsData.weak_bullets.length > 0) {
    if (y > 700) { doc.addPage(); y = 40; }
    y = addSectionHeader(doc, "4. Weak Bullets & Improvements", y);

    atsData.weak_bullets.forEach((bullet: string, idx: number) => {
      if (y > 720) { doc.addPage(); y = 40; }
      doc.setFont("helvetica", "bold");
      doc.setFontSize(9);
      doc.setTextColor(225, 29, 72); // rose-600 original red
      doc.text(`Original Bullet ${idx + 1}:`, 30, y);
      y += 12;

      doc.setFont("helvetica", "normal");
      doc.setTextColor(17, 17, 17);
      const originalLines = doc.splitTextToSize(bullet, 520);
      doc.text(originalLines, 35, y);
      y += originalLines.length * 12 + 2;

      doc.setFont("helvetica", "bold");
      doc.setTextColor(16, 185, 129); // emerald-500 optimized green
      doc.text("Improved (STAR Format):", 30, y);
      y += 12;

      doc.setFont("helvetica", "italic");
      doc.setTextColor(17, 17, 17);
      // Fallback recommendation
      const recommendation = (atsData.improvement_suggestions && atsData.improvement_suggestions[idx]) || 
        "Spearheaded implementation refactoring that resulted in a measurable 30% performance speedup.";
      const improvedLines = doc.splitTextToSize(recommendation, 520);
      doc.text(improvedLines, 35, y);
      y += improvedLines.length * 12 + 15;
    });
  }

  drawFooter(doc);
  doc.save(`syntrix-ats-report-${getFormattedDate()}.pdf`);
};

