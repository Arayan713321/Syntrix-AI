/**
 * 3. Interview Report PDF Export
 */
export const generateInterviewReportPDF = (sessionData: {
  sessionId: string;
  role: string;
  type: string;
  questions: any[];
  answers: Record<number, string>;
  evaluations: Record<number, any>;
}, candidateName: string = "Executive Candidate") => {
  const { doc, startY } = setupPDFDoc(
    "Mock Interview Report",
    `Simulation Q&A Assessment - ${sessionData.type || "Technical"} Track`,
    [
      { label: "Candidate", value: candidateName },
      { label: "Role Context", value: sessionData.role || "Software Engineer" },
      { label: "Session ID", value: sessionData.sessionId || "sess_unknown" },
      { label: "Date", value: new Date().toLocaleDateString() }
    ]
  );

  let y = startY;

  // Calculate Aggregates
  const evaluatedKeys = Object.keys(sessionData.evaluations || {});
  let totalScore = 0;
  let verdict: "Strong" | "Average" | "Needs Work" = "Average";

  if (evaluatedKeys.length > 0) {
    const sum = evaluatedKeys.reduce((acc, key) => acc + (sessionData.evaluations[Number(key)]?.score || 0), 0);
    totalScore = Math.round(sum / evaluatedKeys.length);
  }

  if (totalScore >= 80) verdict = "Strong";
  else if (totalScore >= 60) verdict = "Average";
  else verdict = "Needs Work";

  // Section 1: Overall Performance
  y = addSectionHeader(doc, "1. Overall Performance Summary", y);
  
  const perfRows = [
    ["Average Score", `${totalScore} / 100`],
    ["Performance Verdict", verdict],
    ["Total Completed Questions", `${evaluatedKeys.length} of ${sessionData.questions?.length || 5}`]
  ];

  autoTable(doc, {
    startY: y,
    head: [["Performance Criteria", "Assessment Result"]],
    body: perfRows,
    theme: "striped",
    headStyles: { fillColor: [124, 58, 237], textColor: [255, 255, 255], fontStyle: "bold" },
    bodyStyles: { textColor: [17, 17, 17], fontSize: 9 },
    alternateRowStyles: { fillColor: [249, 249, 249] },
    margin: { left: 20, right: 20 },
    didDrawPage: (data) => {
      y = data.cursor ? data.cursor.y + 25 : y + 90;
    }
  });

  // Section 2: Question by Question breakdown
  if (sessionData.questions && sessionData.questions.length > 0) {
    if (y > 750) { doc.addPage(); y = 40; }
    y = addSectionHeader(doc, "2. Detailed Q&A Breakdown", y);

    sessionData.questions.forEach((q: any, idx: number) => {
      if (y > 680) { doc.addPage(); y = 40; }

      // Q header
      doc.setFont("helvetica", "bold");
      doc.setFontSize(9);
      doc.setTextColor(124, 58, 237); // purple
      doc.text(`QUESTION ${idx + 1}:`, 30, y);
      y += 12;

      doc.setFont("helvetica", "normal");
      doc.setTextColor(17, 17, 17);
      const qTextLines = doc.splitTextToSize(`"${q.question}"`, 520);
      doc.text(qTextLines, 35, y);
      y += qTextLines.length * 12 + 4;

      // Answer
      const answer = sessionData.answers[q.id] || "No response provided.";
      doc.setFont("helvetica", "bold");
      doc.setTextColor(71, 85, 105);
      doc.text("Your Response:", 30, y);
      y += 12;

      doc.setFont("helvetica", "normal");
      doc.setTextColor(17, 17, 17);
      const ansLines = doc.splitTextToSize(`"${answer}"`, 520);
      doc.text(ansLines, 35, y);
      y += ansLines.length * 12 + 4;

      // Score / Verdict subline
      const evalObj = sessionData.evaluations[q.id];
      if (evalObj) {
        doc.setFont("helvetica", "bold");
        doc.setTextColor(17, 17, 17);
        doc.text(`Score: ${evalObj.score || 0}%  |  Confidence: ${evalObj.confidence || evalObj.confidence_rating || 0}%  |  Verdict: ${evalObj.verdict || "N/A"}`, 30, y);
        y += 15;

        // Strengths
        if (evalObj.strengths && evalObj.strengths.length > 0) {
          doc.setFont("helvetica", "bold");
          doc.setTextColor(16, 185, 129); // green
          doc.text("Strengths:", 35, y);
          y += 12;
          doc.setFont("helvetica", "normal");
          doc.setTextColor(17, 17, 17);
          evalObj.strengths.forEach((str: string) => {
            const strLines = doc.splitTextToSize(`• ${str}`, 500);
            doc.text(strLines, 40, y);
            y += strLines.length * 12;
          });
          y += 2;
        }

        // Improvements
        if (evalObj.improvements && evalObj.improvements.length > 0) {
          doc.setFont("helvetica", "bold");
          doc.setTextColor(225, 29, 72); // red
          doc.text("Improvements:", 35, y);
          y += 12;
          doc.setFont("helvetica", "normal");
          doc.setTextColor(17, 17, 17);
          evalObj.improvements.forEach((imp: string) => {
            const impLines = doc.splitTextToSize(`• ${imp}`, 500);
            doc.text(impLines, 40, y);
            y += impLines.length * 12;
          });
          y += 2;
        }

        // STAR rephrase
        const starRephrase = evalObj.starRephrase || evalObj.suggested_formulation;
        if (starRephrase) {
          if (y > 700) { doc.addPage(); y = 40; }
          doc.setFont("helvetica", "bold");
          doc.setTextColor(124, 58, 237);
          doc.text("STAR Formulation Re-phrase:", 35, y);
          y += 12;
          doc.setFont("helvetica", "italic");
          doc.setTextColor(17, 17, 17);
          const starLines = doc.splitTextToSize(starRephrase, 500);
          doc.text(starLines, 40, y);
          y += starLines.length * 12 + 15;
        }
      } else {
        doc.setFont("helvetica", "bold");
        doc.setTextColor(148, 163, 184);
        doc.text("AI Response Evaluation: Not Graded", 30, y);
        y += 25;
      }
    });
  }

  // Section 3: AI Body Language Tips (Static)
  if (y > 660) { doc.addPage(); y = 40; }
  y = addSectionHeader(doc, "3. AI Confidence Body Language tips", y);
  doc.setFont("helvetica", "normal");
  doc.setFontSize(9);
  doc.setTextColor(17, 17, 17);

  const tips = [
    "Maintain steady eye contact with the camera lens, not your monitor or preview panel.",
    "Ensure your workspace is front-lit and your physical background is clear of clutter.",
    "Speak at a measured, deliberate pace, pausing intentionally before key STAR transitions.",
    "Keep your posture straight and open — confident posture naturally filters into high vocal quality."
  ];

  tips.forEach((tip: string) => {
    const tipLines = doc.splitTextToSize(`✔  ${tip}`, 520);
    doc.text(tipLines, 30, y);
    y += tipLines.length * 12;
  });

  drawFooter(doc, "Generated by Syntrix AI Mock Interview Panel Simulator");
  doc.save(`syntrix-interview-report-${getFormattedDate()}.pdf`);
};
